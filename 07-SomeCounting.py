
 # Counting Loops excercise

# Count from 0 to 50 in increments of 1
print("Count up to 0 to 50:")
for i in range(0, 51):
    print(i)
# Count down from 50 to 0 in increments of 1
print("\nCount down from 50 to 0:")
for i in range(50, -1, -1):
    print(i)    
# Count up from 30 to 50 in increments of 1
print("\nCount up from 30 to 50:")
for i in range(30, 51):
    print(i)
# Count down from 50 to 10 by 2
print("\nCount down from 50 to 10 by 2:")
for i in range(50, 9, -2):
    print(i)
# Count up from 100 to 200 by 5
print("\nCount up from 100 to 200 by 5:")
for i in range(100, 201, 5):
    print(i)

# this demonstrates the use of a for loop with range() to create counting loops with different start, stop, and step values.
